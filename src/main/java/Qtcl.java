public class Qtcl {


    private  String cl;
    private  String sb;

    @Override
    public String toString() {
        return "Qtcl{" +
                "cl='" + cl + '\'' +
                ", sb='" + sb + '\'' +
                '}';
    }

    public String getCl() {
        return cl;
    }

    public void setCl(String cl) {
        this.cl = cl;
    }

    public String getSb() {
        return sb;
    }

    public void setSb(String sb) {
        this.sb = sb;
    }
}
